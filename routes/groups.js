router.get('/getAllByUser', function(req, res) {
	var buf = Buffer.from(req.query.data, 'base64');
	var params = JSON.parse(buf);

	var data = {
		status:"OK",
		data:{}
	};

	var connection = new conn.SqlConnection().connection;
	connection.connect(function(err) {
		if (err) {
			console.error('error connecting: ' + err.stack);
			data.status = "false";

			var jData = JSON.stringify(data);
			res.send(new Buffer(jData).toString('base64'));
			connection.end();
			return;
		}

		var query = 
			`SELECT
				c.id,
				c.\`name\`,
				CASE u.users_id
					WHEN ? THEN 1
					WHEN NULL THEN 0 
					ELSE 0
				END AS asignado 
					
			FROM
				class_group c
			LEFT JOIN (
				SELECT
					*
				FROM 
					users_class_groups us
				WHERE us.users_id = ?
			) AS u ON u.class_group_id = c.id`
		;

		var p = [params.userId, params.userId];
		connection.query(query, p , function(err, rows) {
		
			if (err) {
				console.error('error query: ' + query + err.stack);
				data.status = "false";

				var jData = JSON.stringify(data);
				res.send(new Buffer(jData).toString('base64'));
				connection.end();
				return;
			}

			data.data = rows;
			data.status = "true";
			var jData = JSON.stringify(data);
		  	res.send(new Buffer(jData).toString('base64'));
		  	connection.end();
		});
	});
});

